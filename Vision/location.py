# =====================================================
# line_grid_navigation.py
#
# 功能：
# 1. 过线计数
# 2. 网格坐标更新
# 3. 格中心定位
# 4. 稳定停车判断
#
# 适用于：
# K230
# 灰度图
# 网格地图
# =====================================================

import math

# =====================================================
# 地图坐标
# =====================================================

coord_x = 0
coord_y = 0

# =====================================================
# 摄像头图像尺寸（1920x1080）
# =====================================================

IMG_W = 1920
IMG_H = 1080

# =====================================================
# 过线检测线
# =====================================================

COUNT_LINE_Y = 850

# =====================================================
# 格中心目标点
# （摄像头在车头，需要实测标定）
# =====================================================

TARGET_X = 960
TARGET_Y = 760

# =====================================================
# 格中心容差
# =====================================================

CENTER_THRESHOLD = 20

# =====================================================
# 连续稳定帧
# =====================================================

stable_count = 0

# 连续多少帧认为真正到中心
REQUIRED_STABLE_FRAMES = 5

# =====================================================
# 上一帧线位置
# =====================================================

last_line_y = None

# =====================================================
# 过线锁
# 防止重复计数
# =====================================================

cross_lock = False

# =====================================================
# 当前运动方向
#
# 1  = 右
# -1 = 左
# 2  = 下
# -2 = 上
# =====================================================

move_direction = 1

# =====================================================
# 设置方向
# =====================================================

def set_direction(direction):

    global move_direction

    move_direction = direction

# =====================================================
# 更新地图坐标
# =====================================================

def update_coord():

    global coord_x
    global coord_y

    # 向右
    if move_direction == 1:
        coord_x += 1

    # 向左
    elif move_direction == -1:
        coord_x -= 1

    # 向下
    elif move_direction == 2:
        coord_y += 1

    # 向上
    elif move_direction == -2:
        coord_y -= 1

    print("[GRID] POSITION =", coord_x, coord_y)

# =====================================================
# 过线检测
#
# 输入：
# line_y = 当前检测到的黑线y坐标
# =====================================================

def check_cross_line(line_y):

    global last_line_y
    global cross_lock

    # 第一帧初始化
    if last_line_y is None:

        last_line_y = line_y

        return False

    crossed = False

    # =================================================
    # 从上往下穿过检测线
    # =================================================

    if last_line_y < COUNT_LINE_Y and line_y >= COUNT_LINE_Y:

        if not cross_lock:

            crossed = True

            cross_lock = True

            print("[GRID] CROSS LINE")

            update_coord()

    # =================================================
    # 离开检测线后解锁
    # =================================================

    if line_y < COUNT_LINE_Y - 50:

        cross_lock = False

    # 更新上一帧
    last_line_y = line_y

    return crossed

# =====================================================
# 格中心检测
#
# 输入：
# center = (x,y)
# =====================================================

def check_grid_center(center):

    global stable_count

    cx = center[0]
    cy = center[1]

    # =================================================
    # 计算误差
    # =================================================

    error_x = cx - TARGET_X
    error_y = cy - TARGET_Y

    print("[CENTER] error_x =", error_x)
    print("[CENTER] error_y =", error_y)

    # =================================================
    # 判断是否进入中心范围
    # =================================================

    if abs(error_x) < CENTER_THRESHOLD and abs(error_y) < CENTER_THRESHOLD:

        stable_count += 1

        print("[CENTER] stable =", stable_count)

    else:

        stable_count = 0

    # =================================================
    # 连续稳定
    # =================================================

    if stable_count >= REQUIRED_STABLE_FRAMES:

        stable_count = 0

        print("[CENTER] ARRIVED")

        return True

    return False

# =====================================================
# 获取当前位置
# =====================================================

def get_position():

    return (coord_x, coord_y)

# =====================================================
# 重置系统
# =====================================================

def reset_navigation():

    global coord_x
    global coord_y

    global stable_count

    global last_line_y

    global cross_lock

    coord_x = 0
    coord_y = 0

    stable_count = 0

    last_line_y = None

    cross_lock = False

    print("[SYSTEM] RESET")
